console.log("============= Primitive Data Type =============");

console.log('======== 1. Number ========');
var age = 22;
var typeOfAge = typeof age;
console.log("AGE: ", age, " and variable age type is: ", typeOfAge);

var productPrice = 99.67;
console.log("PRICE: ", productPrice, " and variable productPrice type is: ", typeof productPrice);

console.log('======== 2. String ========');
var city = "Pune";
console.log("CITY: ", city);
console.log("city variable type is:", typeof  city);

var bestFriendName = 'Narayan Murti';
console.log("Best Friend: ", bestFriendName, " and variable type is: ", typeof bestFriendName);

console.log('======== 3. Boolean ========');
var isMarried = true;
console.log("Is Married:", isMarried, "and it's type is: ", typeof isMarried);

var isPresent = false;
console.log("Is Present:", isPresent, "and it's type is: ", typeof isPresent);

console.log("============= Special or Trivial Data Type =============");

var graduation;
console.log("Graduation :", graduation, "and it's type is: ", typeof graduation);

var marks = null;
console.log("Marks :", marks, "and it's type is: ", typeof marks);